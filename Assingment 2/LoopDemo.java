public class LoopDemo {
    public static void main(String[] args) {
       
        System.out.println("For loop:");
        for (int i = 1; i <= 5; i++) {
            System.out.println("Number: " + i);
        }

        System.out.println("\nWhile loop:");
        int j = 5;
        while (j >= 1) {
            System.out.println("Number: " + j);
            j--;
        }
    }
}
